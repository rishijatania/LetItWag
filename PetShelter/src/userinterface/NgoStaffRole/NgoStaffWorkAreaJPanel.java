    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Organization org = null;
        for (Organization organization1 : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization1 instanceof PetShelterOrganization){
                org = organization1;
                break;
            }
        }
        if (org!=null){
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("SupplyRequestJPanel ", new SupplyRequestJPanel(userProcessContainer, userAccount, ngoOfficeOrganization, (PetShelterOrganization) org, enterprise, business));
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


