    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        pet=business.getPetDirInstance().getListOfPets().stream().filter(item->item.getPetId().equalsIgnoreCase(reportedPetTextField.getText()))
                .findFirst()
                .orElse(null);
        if(pet==null){
            JOptionPane.showMessageDialog(null, "No such pet Found");
        }
        else{
            animalTypeTextField.setText(pet.getAnimalType());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

