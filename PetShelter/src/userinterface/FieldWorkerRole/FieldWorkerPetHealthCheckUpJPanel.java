    private void reqBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reqBtnActionPerformed
        // TODO add your handling code here:
        if(reportedPetTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pet not selected");
            return;
        }
        Date date = new Date();
        PetHealthCheckUpRequest req=new PetHealthCheckUpRequest();
        req.setPet(pet);
        req.setRequestDate(date);
        req.setUrgency(urgencyCmb.getSelectedItem().toString());
        req.setComments(CommentsTextField1.getText());
        req.setRequestId(reportIDTextField.getText());
        req.setStatus("New");
        req.setSender(account);
        dB4OUtil.storeSystem(business);
        account.getWorkQueue().getWorkRequestList().add(req);
        petShelterOrganization.getWorkQueue().getWorkRequestList().add(req);
        JOptionPane.showMessageDialog(null, "Request raised");
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_reqBtnActionPerformed

